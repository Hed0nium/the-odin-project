
function add7(n) {
    return n + 7;
}

function multiple(n1, n2) {
    return n1 * n2;
}

function capitalize(string1) {
    return string1[0].toUpperCase() + string1.substring(1);
}

function lastLetter(string2) {
    return string2.slice(-1)
}

console.log(add7(1))
console.log(multiple(2, 6))
console.log(capitalize("bingus"))
console.log(lastLetter("zipper"))
